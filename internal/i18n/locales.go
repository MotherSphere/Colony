func T(locale, key string) string {
	if loc, ok := locales[locale]; ok {
		if value, found := loc[key]; found {
			return value
		}
	}
	if loc, ok := locales["en"]; ok {
		if value, found := loc[key]; found {
			return value
		}
	}
	return key
}

func Locales() []string {
	keys := make([]string, 0, len(locales))
	for key := range locales {
		keys = append(keys, key)
	}
	sort.Strings(keys)
	return keys
}
